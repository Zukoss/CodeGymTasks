package pl.codegym.task.task04.task0433;


/* 
Zobaczyć dolary w przyszłości
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        String dollar = "$$$$$$$$$$";
        int i = 0;

        while (i < 10) {
            i++;
            System.out.println(dollar);
        }
    }
}
